number = int(input("enter the number 1 to 7 to get the day:"))
if number == 1:
    print("sunday")
elif number == 2:
    print("monday")
elif number == 3:
    print("tuesday")        
elif number == 4:
    print("wednesday")
elif number == 5:
    print("thursday")    
elif number == 6:
    print("friday")
elif number == 7:
    print("saturday")       
else :
    print("enter a valid number")         